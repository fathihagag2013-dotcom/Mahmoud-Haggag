# ===============================
# Helper function to check valid move
# Works for any maze size
# ===============================
def is_valid(maze, row, col):
    ROWS = len(maze)
    COLS = len(maze[0])
    return (
        0 <= row < ROWS and
        0 <= col < COLS and
        maze[row][col] == 0
    )
